/**
 * 从 utils/mock-data.js 生成 uniCloud 导入用的种子数据。
 *
 * 使用：
 *   node tools/gen-seed-data.js
 *
 * 输出：
 *   uniCloud-aliyun/cloudfunctions/import-seeds/seed-data.generated.js
 */

const fs = require('fs')
const path = require('path')
const vm = require('vm')

const srcPath = path.resolve(__dirname, '../utils/mock-data.js')
const outPath = path.resolve(
  __dirname,
  '../uniCloud-aliyun/cloudfunctions/import-seeds/seed-data.generated.js'
)

const source = fs.readFileSync(srcPath, 'utf8')

// 将 ES Module 的 export const xxx 转为 module.exports 后用 vm 执行
const transpiled = source
  .replace(/^export\s+const\s+/gm, 'module.exports.')
  .replace(/^export\s+function\s+(\w+)/gm, 'module.exports.$1 = function $1')
  .replace(/^export\s+default\s+/gm, 'module.exports.default = ')

const sandboxModule = { exports: {} }
const ctx = {
  module: sandboxModule,
  exports: sandboxModule.exports,
  require,
  Date,
  console
}
// 修正赋值语法：module.exports.bannerList = [...]
const fixed = transpiled.replace(/^module\.exports\.(\w+)\s*=/gm, 'module.exports.$1 =')

vm.createContext(ctx)
vm.runInContext(fixed, ctx, { filename: 'mock-data.js' })

const exported = sandboxModule.exports

const articles = (exported.articleList || []).map((a) => ({
  seed_id: a._id,
  title: a.title,
  summary: a.summary || '',
  content: a.content || '',
  category: a.category,
  cover_image: a.cover_image,
  author: '编辑部',
  read_count: 0,
  like_count: 0,
  sort_order: 0,
  status: 1,
  create_time: a.create_time || Date.now()
}))

const qa = (exported.qaList || []).map((q, idx) => ({
  seed_id: q._id || `qa${idx + 1}`,
  question: q.question || q.title || '',
  answer: q.answer || q.content || '',
  category: q.category || '',
  asker_name: '匿名网友',
  answerer_name: '编辑部',
  view_count: 0,
  sort_order: idx,
  status: 1,
  create_time: q.create_time || Date.now()
}))

const tips = (exported.tipsList || []).map((t, idx) => ({
  seed_id: t.id || `tip${idx + 1}`,
  title: t.title || '',
  content: t.content || '',
  category: t.category || '',
  sort_order: idx,
  status: 1,
  create_time: Date.now()
}))

const banners = (exported.bannerList || []).map((b) => ({
  seed_id: b.id,
  image: b.image,
  title: b.title,
  link: b.link,
  article_seed_id: b.articleId || ''
}))

const out = `/* eslint-disable */
/**
 * Generated by tools/gen-seed-data.js — DO NOT EDIT BY HAND
 * Source: utils/mock-data.js
 * Generated at: ${new Date().toISOString()}
 */
module.exports = {
  articles: ${JSON.stringify(articles, null, 2)},
  qa: ${JSON.stringify(qa, null, 2)},
  tips: ${JSON.stringify(tips, null, 2)},
  banners: ${JSON.stringify(banners, null, 2)}
}
`

fs.writeFileSync(outPath, out, 'utf8')
console.log(
  `[gen-seed-data] articles=${articles.length} qa=${qa.length} tips=${tips.length} banners=${banners.length}`
)
console.log('[gen-seed-data] wrote', outPath)
